package com.anubhav;

public class Basics {
    public static void main(String[] args) {

        // IF STATEMENT

        int a = 10;
        if (a == 10) {
            System.out.println("Hello World");
        }

        // WHILE LOOPS

        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }

        // FOR LOOPS

        for (int i = 1; i != 5; i++) {
            System.out.println(i);
        }
    }
}
